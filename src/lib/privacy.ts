import { db } from '@/lib/db'

export type PrivacyContentPublic = {
  title: string
  intro: string | null
  content: string
  updatedAt: string
}

/**
 * Возвращает контент политики конфиденциальности.
 * Если в БД нет записи — создаёт дефолтную.
 */
export async function getPrivacyContent(): Promise<PrivacyContentPublic> {
  let row = await db.privacyContent.findUnique({ where: { id: 'default' } })
  if (!row) {
    row = await db.privacyContent.create({
      data: {
        id: 'default',
        title: 'Политика конфиденциальности',
        intro:
          'Настоящая Политика конфиденциальности определяет, каким образом наша компания собирает, использует, хранит и защищает персональные данные, которые вы предоставляете нам при использовании нашего сайта.',
        content: getDefaultContent(),
      },
    })
  }
  return {
    title: row.title,
    intro: row.intro,
    content: row.content,
    updatedAt: row.updatedAt.toISOString(),
  }
}

export async function getRawPrivacyContent() {
  let row = await db.privacyContent.findUnique({ where: { id: 'default' } })
  if (!row) {
    row = await db.privacyContent.create({
      data: {
        id: 'default',
        title: 'Политика конфиденциальности',
        intro:
          'Настоящая Политика конфиденциальности определяет, каким образом наша компания собирает, использует, хранит и защищает персональные данные, которые вы предоставляете нам при использовании нашего сайта.',
        content: getDefaultContent(),
      },
    })
  }
  return row
}

export type PrivacyContentUpdate = Partial<{
  title: string
  intro: string | null
  content: string
}>

export async function updatePrivacyContent(data: PrivacyContentUpdate): Promise<void> {
  await db.privacyContent.upsert({
    where: { id: 'default' },
    update: data,
    create: {
      id: 'default',
      title: data.title ?? 'Политика конфиденциальности',
      intro: data.intro ?? null,
      content: data.content ?? '',
    },
  })
}

